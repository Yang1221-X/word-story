async function genAPI(){
  var endpoint=document.getElementById('ape').value.trim()||'https://api.openai.com/v1/chat/completions';
  var apiKey=document.getElementById('apk').value.trim();
  if(!apiKey){showToast('请先输入 API Key');return;}
  var status=document.getElementById('ais');status.textContent='⏳ 调用 GPT 生成中...';
  try{
    var wordList=curWords.map(function(w){return w[0]+' ('+w[1]+')';}).join(', ');
    var prompt='Write a short engaging story (8-12 sentences) in English that naturally incorporates ALL these vocabulary words: '+wordList+'. For each target word, wrap it in [[word]]. Output ONLY valid JSON (no markdown): {"title":"story title","introEn":"...","sentences":[{"en":"English with [[word]]...","zh":"地道中文翻译","struct":"S+V+O 主谓宾","g":"HTML grammar breakdown"}]}. B1-C1, natural translations.';
    var resp=await fetch(endpoint,{method:'POST',headers:{'Content-Type':'application/json','Authorization':'Bearer '+apiKey},body:JSON.stringify({model:'gpt-4o-mini',messages:[{role:'user',content:prompt}],temperature:0.8,max_tokens:2000})});
    if(!resp.ok)throw new Error('API '+resp.status);
    var data=await resp.json(),content=data.choices[0].message.content;
    content=content.replace(/```json\s*/gi,'').replace(/```/g,'').trim();
    var json=JSON.parse(content);
    curStory={title:json.title||'AI Story',sentences:[],words:curWords,level:curLevel};
    if(json.introEn)curStory.sentences.push({wi:-1,en:json.introEn,zh:json.introZh||'',struct:'开场句',g:''});
    (json.sentences||[]).forEach(function(s){
      var en=s.en.replace(/\[\[([^\]]+)\]\]/g,function(m,word){var found=curWords.find(function(w){return w[0]===word;});return found?wordSpan(found):'<b>'+word+'</b>';});
      curStory.sentences.push({wi:-1,en:en,zh:s.zh||'',struct:s.struct||'',g:s.g||''});
    });
    markUsed(curLevel,curWords);
    renderWords();renderStory();renderLevelBar();status.textContent='✅ 已生成！';
    localStorage.setItem('ws_apikey_v4',apiKey);localStorage.setItem('ws_apiendpoint_v4',endpoint);
  }catch(e){status.textContent='❌ 失败：'+e.message;}
}

