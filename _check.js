// Track used words per level
function getUsed(level){return JSON.parse(localStorage.getItem('ws_used_'+level)||'[]');}
function markUsed(level,words){
  var u=getUsed(level);
  words.forEach(function(w){if(u.indexOf(w[0])===-1)u.push(w[0]);});
  localStorage.setItem('ws_used_'+level,JSON.stringify(u));
}
function resetUsed(level){localStorage.removeItem('ws_used_'+level);}

// ============ WORD PICKING (prefer unused) ============
function pickWords(level,n){
  var all=WB[level].slice(),used=getUsed(level);
  var unused=[],usedPool=[];
  all.forEach(function(w){if(used.indexOf(w[0])===-1)unused.push(w);else usedPool.push(w);});
  var pool=unused.length>=n?unused:unused.concat(usedPool);
  var picked=[];
  for(var i=0;i<n&&pool.length>0;i++){
    var idx=Math.floor(Math.random()*pool.length);
    picked.push(pool.splice(idx,1)[0]);
  }
  return picked;
}

// ============ HELPERS ============
function wordSpan(w){return'<span class="hw" data-w="'+w[0]+'" data-zh="'+w[1]+'" data-pos="'+w[2]+'" data-ex="'+escapeHTML(w[3])+'" onclick="showWordPopup(event,this)">'+w[0]+'</span>';}
function escapeHTML(s){return s.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');}
function unescapeHTML(s){return s.replace(/&amp;/g,'&').replace(/&lt;/g,'<').replace(/&gt;/g,'>').replace(/&quot;/g,'"');}

// ============ STORY GENERATION ============
function genStoryLocal(words){
  var byPos={n:[],v:[],adj:[],adv:[]};
  words.forEach(function(w){
    var pos=w[2];
    if(pos==='n.')byPos.n.push(w);else if(pos==='v.')byPos.v.push(w);
    else if(pos==='adj.')byPos.adj.push(w);else if(pos==='adv.')byPos.adv.push(w);
    else byPos.n.push(w);
  });
  var fw=FRAMEWORKS[Math.floor(Math.random()*FRAMEWORKS.length)];
  var outro=OUTROS[Math.floor(Math.random()*OUTROS.length)];
  var sentences=[{wi:-1,en:fw.en,zh:fw.zh,struct:'开场句 · Opening',g:''}];
  var usedP={n:0,v:0,adj:0,adv:0};
  words.forEach(function(w,i){
    var pos=w[2],cat=pos==='n.'?'n':pos==='v.'?'v':pos==='adj.'?'adj':pos==='adv.'?'adv':'n';
    var patterns=PATTERNS[cat]||PATTERNS['n'];
    var pi=usedP[cat]%patterns.length;usedP[cat]++;
    var pat=patterns[pi];
    sentences.push({wi:i,en:pat.en.replace('{WORD}',wordSpan(w)),zh:pat.zh.replace('{WORD}',w[1]),struct:pat.struct,g:pat.g.replace('{WORD}','<b>'+w[0]+'</b>')});
  });
  sentences.push({wi:-1,en:outro.en,zh:outro.zh,struct:'结尾句 · Closing',g:''});
  return{title:fw.title,sentences:sentences,words:words,level:curLevel};
}

