  var top=rect.bottom+8,left=rect.left+rect.width/2;
  if(top+200>window.innerHeight)top=rect.top-210;
  if(left+180>window.innerWidth)left=window.innerWidth-190;
  if(left<10)left=10;
  popup.style.top=top+'px';popup.style.left=left+'px';
